    void fillTable(List<chuyenDeEntity> list) {
        model = (DefaultTableModel) tblChuyenDe.getModel();
        model.setRowCount(0);
        for (chuyenDeEntity cd : list) {
            Object[] row = new Object[]{cd.getMaCD(), cd.getTenCD(), cd.getHocPhi(), cd.getThoiLuong(), cd.getHinh(), cd.getMoTa()};
            model.addRow(row);
        }
    }

    chuyenDeEntity getForm() {
        String ma, ten, mota, hinh, Url;
        double hocPhi;
        int thoiluong;
        ma = txtMaCD.getText();
        ten = txtTen.getText();
        mota = txtMoTa.getText();
        hocPhi = Double.parseDouble(txtHocPhi.getText());
        thoiluong = Integer.parseInt(txtThoiGian.getText());
        hinh = tenAnh;
        Url = duongdananh;
        return new chuyenDeEntity(ma, ten, hocPhi, thoiluong, hinh, mota, Url);
    }

    void setForm(chuyenDeEntity cd) {
        txtMaCD.setText(cd.getMaCD());
        txtTen.setText(cd.getTenCD());
        txtThoiGian.setText(String.valueOf(cd.getThoiLuong()));
        txtHocPhi.setText(String.valueOf(cd.getHocPhi()));
        txtMoTa.setText(cd.getMoTa());
        lblHinh.setIcon(resizeImage(cd.getUrl()));
    }

    public ImageIcon resizeImage(String ImagePath) {
        ImageIcon myImage = new ImageIcon(ImagePath);
        Image img = myImage.getImage();
        Image newImg = img.getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }

    boolean checkForm() {
        if (txtMaCD.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trồng mã");
            return false;
        } else if (txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trồng tên");
            return false;
        } else if (txtThoiGian.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trồng thời gian");
            return false;
        } else if (txtHocPhi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trồng Học phí");
            return false;
        }
        return true;
    }

