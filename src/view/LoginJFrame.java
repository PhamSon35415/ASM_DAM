    boolean checkform() {
        if (txtUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin tài khoản");
            return false;
        } else if (new String(txtPassWord.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin tài khoản");
            return false;
        } else {
            return true;
        }
    }

    String manv() {
        System.out.println(ss.dangnhap(txtUser.getText()).getMaNV());
        return ss.dangnhap(txtUser.getText()).getMaNV();
    }

